let num1 = 4.4;
let num2 = 4.5;

console.log("Math.round(num1)", Math.round(num1));
console.log("Math.round(num2)", Math.round(num2));

console.log("Math.ceil(num1)", Math.ceil(num1));
console.log("Math.ceil(num2)", Math.ceil(num2));

console.log("Math.floor(num1)", Math.floor(num1));
console.log("Math.floor(num2)", Math.floor(num2));

// trunc -> return the integer part of the number
console.log("Math.trunc(num1)", Math.trunc(num1));
console.log("Math.trunc(num2)", Math.trunc(num2));

//max min -> find highest or lowest element in the list

console.log("Math.max(1,2,3,4)", Math.max(1, 2, 3, 4));
console.log("Math.min(1,2,3,4)", Math.min(1, 2, 3, 4));
